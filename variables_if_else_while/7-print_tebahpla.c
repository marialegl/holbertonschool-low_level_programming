#include <stdio.h>

/**
 * main - Entry point of the program
 *
 * This function generates a random number and determines
 * whether it's positive, negative, or zero.
 *
 * Return: Always 0 (indicating success)
 */
int main(void)
{

	int i;

	for (i = 122; i >= 97; --i)
	{
		printf("%c", i);
	}
	printf("\n");

	return (0);
}
